using System;


public class Reference
{
    private string book;
    private int chapter;
    private int verse;

    public string Book
    {
        get { return book; }
        set { book = value; }
    }

    public int Chapter
    {
        get { return chapter; }
        set { chapter = value; }
    }

    public int Verse
    {
        get { return verse; }
        set { verse = value; }
    }
}
